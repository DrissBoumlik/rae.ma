<p>The KhAI is entitled to conduct the training of PhD students on the large list of major subjects including those related to aerospace area. The indicated list is attached below. Those major subjects on which the defense of dissertation is possible to be realized at the KhAI are marked with a sign * in the list. The KhAI has got a certificate of Higher Certifying Commission on each specialty.</p>
<p>The training on the full-time post-graduate course lasts 3 years. The graduates who are going to enter the post-graduate course should pass the entrance examinations on major subject, foreign language (Russian or English) and philosophy.</p>
<p>Within the first year of study the post-graduate students have to pass candidate examinations – candidate minimums which are obligatory under defense of dissertation.</p>
<p>Candidate minimums shall be passed on the following subjects: major subject, foreign subject, philosophy.</p>
<p>Each PhD student shall work individually under the guidance of his/her research supervisor. Within the first year of training an aspirant jointly with his/her research supervisor shall definitely formulate the theme and tasks of investigations.</p>
<p>Within the whole period of training an PhD student has to perform reference sources survey on world publications, to carry out theoretical and experimental research work, to make analysis of research results, to prepare and publish at least 2 – 3 research articles.</p>
<p>At the end of training on the post-graduate course an aspirant shall submit dissertation on 120 x 150 A4-size sheets and to defend it in public. The public defense envisages getting positive opinions about research results from a number of scientific schools and enterprises of aerospace industry of Ukraine and other countries.</p>
<p>The defense procedure of already prepared and submitted work usually takes 2 – 4 months in dependence on the level of preparation of the . The presence of the PhD student at his/her defense is obligatory.</p>
<p>According to the results of successful defense the KhAI issues a diploma certifying the awarding of a degree of Candidate of Science of state sample to the aspirant.</p>
