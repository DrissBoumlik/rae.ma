<p><strong>Medical (THERAPEUTIC) FACULTY</strong></p>
<p>Therapeutic&nbsp;faculty (medical) was founded in 1906. It was&nbsp;<strong>Russian first</strong>&nbsp;Higher Medical educational establishment for women. Over the period of its existence there were more than 30000 therapists trained.</p>
<p>Currently, therapeutic faculty is still the largest university faculty – 4630 students of university are specialized in «Clinical Therapy». As it was in previous years faculty departments are tightly connected with Moscow institutions for health treatment in practice, such as: big state clinical hospitals, maternity hospitals, outpatient-polyclinic establishments that form the clinical basis.</p>
<p>Russian National Research Medical University named after N.I. Pirogov trains highly qualified physicians on major spheres of medical activity:&nbsp;therapists, surgeons and others. The faculty consists of 40 departments that provide our graduates for high level in general theories, medico biological and clinical training.</p>
<p>&nbsp;</p>
<p><strong>PEDIATRIC FACULTY</strong></p>
<p>In 1930 the unique to the world and to our country maternity, baby-hood and children’s treatment faculty was opened in RNRMU&nbsp;(now pediatric faculty).</p>
<p>Starting from the very day of the faculty formation future pediatricians were trained according to high school regulations and practical health treatment demands. Departments of the pediatric faculty are tightly connected with Moscow Health Service establishment and possess hospital stock of more than 10000 beds.</p>
<p>22 academicians and RAMS associates as well as 20 academicians and&nbsp;associates of other academies, more than 130 doctors of medical sciences including 96 professors 53 of whom head the departments work at the departments and train students of the pediatric faculty. More than 400 lecturers have academic degrees of candidate of Medical Science, over 190 have a degree of an associate professor.</p>
<p>Over the period of pediatric faculty existence there were 75 full-time and 37 evening courses of study, over 25000 pediatricians were trained.</p>
